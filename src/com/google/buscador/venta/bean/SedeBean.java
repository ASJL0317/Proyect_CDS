package com.google.buscador.venta.bean;

public class SedeBean {

	private int codSede;
	private String nomSede, dirSede, fechaSede, gerSede, disSede;
	public int getCodSede() {
		return codSede;
	}
	public void setCodSede(int codSede) {
		this.codSede = codSede;
	}
	public String getNomSede() {
		return nomSede;
	}
	public void setNomSede(String nomSede) {
		this.nomSede = nomSede;
	}
	public String getDirSede() {
		return dirSede;
	}
	public void setDirSede(String dirSede) {
		this.dirSede = dirSede;
	}
	public String getFechaSede() {
		return fechaSede;
	}
	public void setFechaSede(String fechaSede) {
		this.fechaSede = fechaSede;
	}
	public String getGerSede() {
		return gerSede;
	}
	public void setGerSede(String gerSede) {
		this.gerSede = gerSede;
	}
	public String getDisSede() {
		return disSede;
	}
	public void setDisSede(String disSede) {
		this.disSede = disSede;
	}
	
	
}
